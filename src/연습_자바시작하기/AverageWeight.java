package 연습_자바시작하기;

public class AverageWeight {
    public static void main(String[] args) {

        //변수 생성
        double a = 81.36;
        double b = a+0.71;
        double c = b-0.43;

        //계산
        double average = (a+b+c)/3;

        //출력
        System.out.println(average + "kg");


    }
}
